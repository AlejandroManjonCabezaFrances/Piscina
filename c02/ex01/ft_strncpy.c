#include<stdio.h>
#include<unistd.h>
#include<string.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] && i < n)
	{	
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main(void)
{
	char	dest[30] = "hola";;
	char	src[5] = "patos";
	char	*pointer = ft_strncpy(dest, src, 10);
	printf("%s", pointer);
	return(0);
}
